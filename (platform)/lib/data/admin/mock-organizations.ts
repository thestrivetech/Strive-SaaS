/**
 * Helper function to get organization statistics
 */
export function getOrganizationStats() {
  const total = MOCK_ORGANIZATIONS.length;
  const active = MOCK_ORGANIZATIONS.filter((org) => org.subscription_status === 'ACTIVE').length;
  const totalRevenue = MOCK_ORGANIZATIONS.reduce((sum, org) => sum + org.monthly_revenue, 0);
  const totalMembers = MOCK_ORGANIZATIONS.reduce((sum, org) => sum + org.member_count, 0);

  return {
    total,
    active,
    totalRevenue,
    totalMembers,
    avgMembersPerOrg: Math.round(totalMembers / total),
  };
}
