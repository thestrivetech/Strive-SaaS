/**
 * Get audit log statistics
 */
export function getAuditLogStats() {
  const total = MOCK_AUDIT_LOGS.length;

  // Count by day
  const now = new Date();
  const today = new Date(now.toDateString());
  const thisWeek = new Date(today);
  thisWeek.setDate(today.getDate() - 7);

  const todayCount = MOCK_AUDIT_LOGS.filter(
    (log) => new Date(log.timestamp) >= today
  ).length;

  const thisWeekCount = MOCK_AUDIT_LOGS.filter(
    (log) => new Date(log.timestamp) >= thisWeek
  ).length;

  // Group by action
  const byAction: Record<string, number> = {};
  MOCK_AUDIT_LOGS.forEach((log) => {
    byAction[log.action] = (byAction[log.action] || 0) + 1;
  });

  // Group by severity
  const bySeverity = {
    INFO: MOCK_AUDIT_LOGS.filter((log) => log.severity === 'INFO').length,
    WARNING: MOCK_AUDIT_LOGS.filter((log) => log.severity === 'WARNING').length,
    ERROR: MOCK_AUDIT_LOGS.filter((log) => log.severity === 'ERROR').length,
    CRITICAL: MOCK_AUDIT_LOGS.filter((log) => log.severity === 'CRITICAL').length,
  };

  return {
    total,
    today: todayCount,
    thisWeek: thisWeekCount,
    byAction,
    bySeverity,
  };
}

/**
 * Get recent audit logs
 */
export function getRecentAuditLogs(limit: number = 10): MockAuditLog[] {
  return MOCK_AUDIT_LOGS
    .sort((a, b) => new Date(b.timestamp).getTime() - new Date(a.timestamp).getTime())
    .slice(0, limit);
}
